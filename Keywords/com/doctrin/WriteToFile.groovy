public class WriteToFile {
	String path=RunConfiguration.getProjectDir()+"/Output.txt"
	private boolean append_to_file = true;

	@Keyword
	public void WriteParamToFile (String name, String title) throws IOException {
		FileWriter write = new FileWriter(path, append_to_file);
		PrintWriter print_line = new PrintWriter(write);
		print_line.printf("%s" + "%n", name + ", "+title);
		print_line.close();
	}
}

